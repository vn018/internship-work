def read_elements(file):
  ans = []
  for i in file:
    sub_ans = i[:-1].split(' ')
    for j in sub_ans:
        ans.append(j)
  
  return ans

source = open("source.txt","r")
read_source = source.readlines()

elements_source = read_elements(read_source)
elements_source = [str.lower(i) for i in elements_source]
print(elements_source)

destination = open("destination.txt","r")
read_destination = destination.readlines()

elements_destination = read_elements(read_destination)
elements_destination = [str.lower(i) for i in elements_destination]
print(elements_destination)

