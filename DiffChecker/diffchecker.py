"""# Diff-Checker"""

def diffcheck(source , destination):
  s = [i for i in source]
  d = [i for i in destination]
  for i in d:
    if(s.count(i) > 0):
      s[s.index(i)] = " "
      d[d.index(i)] = " "
    # else:
    #   d[destination.index(i)] += " - not found"

  return s,d

